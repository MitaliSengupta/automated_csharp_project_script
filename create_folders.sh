#!/usr/bin/env bash

for name in $@
do
    echo -------------------------------------------------------------------------------------------
    echo ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^ CREATING FOLDER ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
    echo -------------------------------------------------------------------------------------------
    dotnet new console -o $name
    echo -------------------------------------------------------------------------------------------
    echo ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^ RENAMING .CS FILES ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
    echo -------------------------------------------------------------------------------------------
    mv ./$name/Program.cs ./$name/"$name".cs
    echo -------------------------------------------------------------------------------------------
    echo ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^ CREATING MAIN.CS FILES ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
    echo -------------------------------------------------------------------------------------------
    var="$(echo $name | cut -d '-' -f 1)"
    touch ./$name/"${var}"-main.cs
done
echo -------------------------------------------------------------------------------------------
echo ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^ ALL DONE, ALL SET! ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
echo -------------------------------------------------------------------------------------------
